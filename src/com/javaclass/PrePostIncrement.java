package com.javaclass;

public class PrePostIncrement {
	
	public static void main(String[] args) {
		
		
		int i = 20;
		
		
		System.out.println("    Increment   ");
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i);
		
		
		System.out.println("    Decrement  ");
		System.out.println(i--);
		System.out.println(i);
		System.out.println(--i);
		System.out.println(i);
	}

}
